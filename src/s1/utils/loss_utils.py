def feat_sim(query, key, logit_scale=100):
    # inp and out are of shape (num, emb_dim)
    norm_query  = query / query.norm(p=2, dim=-1, keepdim=True)
    norm_key = key / key.norm(p=2, dim=-1, keepdim=True)
    logits = logit_scale * torch.matmul(norm_query, norm_key.t())
    return logits.squeeze()

